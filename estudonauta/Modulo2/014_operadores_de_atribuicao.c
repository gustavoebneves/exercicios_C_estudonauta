#include <stdio.h>

void main()
{
    int a, b, c, d, e;

    a = 5;
    a = a + 3;
    printf("%i\n", a);

    a = 5;
    a += 3;
    printf("%i\n\n", a);

// -------------------------------------------------------------------------------------------------------------------

    b = 5;
    b = b - 3;
    printf("%i\n", b);

    b = 5;
    b -= 3;
    printf("%i\n\n", b);

// -------------------------------------------------------------------------------------------------------------------

    c = 5;
    c = c * 3;
    printf("%i\n", c);

    c = 5;
    c *= 3;
    printf("%i\n\n", c);

// -------------------------------------------------------------------------------------------------------------------

    d = 6;
    d = d / 3;
    printf("%i\n", d);

    d = 6;
    d /= 3;
    printf("%i\n\n", d);

// -------------------------------------------------------------------------------------------------------------------

    e = 7;
    e = e % 3;
    printf("%i\n", e);

    e = 7;
    e %= 3;
    printf("%i\n\n", e);

}

/*
Operadores de atribuição

x = x + 3 -> x += 3 (funciona com "-" também)

k = k * 4 -> k *= 4 

j = j / 2 -> j /= 2

m = m % 5 -> m %= 5
*/